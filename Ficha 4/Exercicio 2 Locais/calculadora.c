long soma(int num1, int num2) {
    return num1 + num2;
}

long subtracao(int num1, int num2) {
    return num1 - num2;
}

long multiplicacao(int num1, int num2) {
    return num1 * num2;
}

long divisao(int num1, int num2) {
    return (float) num1 / num2;
}